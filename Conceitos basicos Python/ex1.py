# Faça um Programa que peça dois números, realize as principais operações
# soma, subtração, multiplicação, divisão

num1 = float(input('Informe um numero :'))
num2 = float(input('Informe outro numero: '))

print(f'A adicao dos numeros : {num1 + num2}')
print(f'A subtracao dos numeros : {num1 - num2}')
print(f'A multiplicacao dos numeros : {num1 * num2}')
print(f'A divisao dos numeros : {num1 // num2}')