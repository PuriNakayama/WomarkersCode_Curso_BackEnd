# Faça um Programa que peça a quantidade de quilômetros, transforme em 
# metros, centímetros e milímetros

km = float(input('Informe uma quantidade de quilometros :'))

print(f'A quantidade de Km em metros eh {km * 1000}')
print(f'A quantidade de Km em centimetros eh {km * 100000}')
print(f'A quantidade de Km em milimetros eh {km * 1000000}')